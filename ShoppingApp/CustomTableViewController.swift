        let cell = tableView.dequeueReusableCell(withIdentifier: "CategoryTableViewCell", for: indexPath) as! CategoryTableViewCell
        
        cell.nameLabel.text = "\(indexPath.row)"
        cell.carButton.tag = indexPath.row
        cell.carButton.addTarget(self, action: #selector(showDetailedDescription), for: .touchUpInside)
        
        return cell
    }
    
    @objc func showDetailedDescription(sender: UIButton) {
        let indexP = IndexPath(row: sender.tag, section: 0)
        let detailsViewController = self.storyboard?.instantiateViewController(withIdentifier: "DetailsViewController") as! DetailsViewController
        detailsViewController.result = nameArr[indexP.row]
        self.navigationController?.pushViewController(detailsViewController, animated: true)
        
